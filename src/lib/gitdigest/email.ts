import { Resend } from "resend"
import type { DigestReport } from "./ai"

function getResendClient() {
  const apiKey = process.env.RESEND_API_KEY
  if (!apiKey) {
    throw new Error("RESEND_API_KEY environment variable is not set")
  }
  return new Resend(apiKey)
}

export interface EmailOptions {
  to: string
  subject?: string
}

export async function sendReportEmail(
  report: DigestReport,
  username: string,
  options: EmailOptions
) {
  const startDate = new Date(report.dateRange.start).toLocaleDateString(
    "en-US",
    { month: "short", day: "numeric" }
  )
  const endDate = new Date(report.dateRange.end).toLocaleDateString("en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
  })

  const subject =
    options.subject || `Dev Report: ${username} (${startDate} - ${endDate})`

  const html = generateEmailHtml(report, username, startDate, endDate)

  const resend = getResendClient()
  const { data, error } = await resend.emails.send({
    from: "GitDigest <gitdigest@tylerschmidt.dev>",
    to: options.to,
    subject,
    html,
  })

  if (error) {
    throw new Error(`Failed to send email: ${error.message}`)
  }

  return data
}

